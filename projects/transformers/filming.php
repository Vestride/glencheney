    <h2>Filming</h2>
    <p>
        Filming began in Los Angeles, California, in May 2008. The former Hughes Aircraft soundstages at Playa Vista served for filming the majority of interior scenes. From June 2, three days were spent on an action sequence at the Bethlehem Steel site in Bethlehem, Pennsylvania, which was used to represent a portion of Shanghai. Afterwards, they shot at the Steven F. Udvar-Hazy Center. The crew moved to Philadelphia on June 9, where they shot at the defunct PECO Richmond power station, the University of Pennsylvania, Drexel University, the Eastern State Penitentiary, Fairmount Park, Philadelphia City Hall, Rittenhouse Square, and historic Chancellor Street.
    </p>
    <br />
    <p>
        The crew shot at Holloman Air Force Base and White Sands Missile Range in New Mexico during September. The two locations were used for Qatar in the 2007 film, and stood in for Egypt in this film. A scale model in Los Angeles was also used for some close-ups of the pyramids. Shooting at Tucson International Airport and the 309th Aerospace Maintenance and Regeneration Group's aircraft boneyard took place in October.
    </p>
    <br />
    <p>
        The first unit (including Shia LaBeouf) then shot for three days in Egypt itself, at the Giza pyramid complex and Luxor. For security's sake, the shoot was highly secretive, but according to Lorenzo di Bonaventura, a crew of 150 Americans and &quot;several dozen local Egyptians&quot; ensured a &quot;remarkably smooth&quot; shoot. Bay earned the Egyptian government's approval to film at the pyramids by contacting Zahi Hawass, who Bay recalled &quot;put his arm around me and said, 'Don't hurt my pyramids.'&quot; A fifty foot tall camera crane was used at the location. Four days were then spent in Jordan; the Royal Jordanian Air Force aided in filming at Petra, Wadi Rum and Salt because one of the country's princes liked the 2007 film. Filming continued at the Place de la Concorde in Paris, with second unit shots of the Eiffel Tower and the Arc de Triomphe. The cast and crew finished on the aircraft carrier USS John C. Stennis on November 2, 2008.
    </p>
