    <h2>Reviews</h2>
    <p>
        The film received &quot;generally unfavorable&quot; reviews from film critics. Based on 223 reviews collected by Rotten Tomatoes, Revenge of the Fallen received an average of 19% overall approval rating. By comparison, Metacritic calculated an average score of 35 out of 100 from the 32 reviews it collected. In contrast to the negative critical reception, audience reaction was mostly positive; CinemaScore polls reported that on a scale of A+ to F, the average grade cinemagoers gave the film was &quot;B+&quot;.
