    <p>
        According to The Washington Post, Transformers: Revenge of the Fallen is Bay's worst-reviewed film, faring even lower than Pearl Harbor. Ray Bennett of The Hollywood Reporter commented in his review that &quot;for the uninitiated, it's loud, tedious, and at 147 minutes, way too long.&quot; Roger Ebert, who had given the 2007 film three stars, gave Revenge of the Fallen only one star, calling it &quot;...a horrible experience of unbearable length.&quot; He later wrote in his blog about the film, saying &quot;The day will come when Transformers: Revenge of the Fallen will be studied in film classes and shown at cult film festivals. It will be seen, in retrospect, as marking the end of an era. Of course there will be many more CGI-based action epics, but never again one this bloated, excessive, incomprehensible, long (149 minutes) or expensive ($190 million).&quot; Rolling Stone critic Peter Travers did not give the film any stars considering that &quot;Transformers 2 has a shot at the title Worst Movie of the Decade.&quot; The A.V. Club gave the film a C-.
    </p>
    <br />
    <p>
        There has also been considerable negative reaction to the characters Mudflap and Skids, who are alleged to embody racist stereotypes. Manohla Dargis of The New York Times said that &quot;the characters [...] indicate that minstrelsy remains as much in fashion in Hollywood as when, well, Jar Jar Binks was set loose by George Lucas.&quot; <span class="quote">Critic Scott Mendelson said, &quot;To say that these two are the most astonishingly racist caricatures that I've ever seen in a mainstream motion picture would be an understatement.&quot;</span> Harry Knowles, founder of Ain't It Cool News, went further, asking his readers &quot;not to support this film&quot; because &quot;you'll be taking [your children] to see a film with the lowest forms of humor, stereotypes and racism around.&quot; Director Bay has attempted to defend the film as &quot;good clean fun&quot; and insisted that &quot;We're just putting more personality in.&quot; Roberto Orci and Alex Kurtzman responded to the controversy with &quot;It&rsquo;s really hard for us to sit here and try to justify it. I think that would be very foolish, and if someone wants to be offended by it, it&rsquo;s their right. We were very surprised when we saw it, too, and it&rsquo;s a choice that was made. If anything, it just shows you that we don&rsquo;t control every aspect of the movie.&quot;
