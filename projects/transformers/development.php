    	<h2>Development</h2>
        <p>
        In September 2007, Paramount announced a late June 2009 release date for the sequel to Transformers. A major hurdle that was overcome during the film's production was the 2007&ndash;2008 Writers Guild of America strike, as well as possible strikes by the Directors Guild of America and the Screen Actors Guild. Bay began creating animatics of action sequences featuring characters rejected for the 2007 film. This would allow animators to complete sequences if the Directors Guild of America went on strike in July 2008, which ultimately did not happen. The director considered making a small project in between Transformers and its sequel, but knew &quot;you have your baby and you don't want someone else to take it&quot;. The film was given a $200 million budget, which was $50 million more than the 2007 film, and some of the action scenes rejected for the original were written into the sequel, such as the way Optimus is reintroduced in this film. Lorenzo di Bonaventura said the studio proposed filming two sequels simultaneously, but he and Bay concurred that was not the right direction for the series.
		</p>
        <br />
        <p>
Writers Roberto Orci and Alex Kurtzman originally passed on the sequel because of a busy schedule. The studio began courting other writers in May 2007, but as they were unimpressed with their pitches, they convinced Orci and Kurtzman to return. The studio also signed on Ehren Kruger, as he impressed Bay and Hasbro president Brian Goldner with his knowledge of the Transformers mythology, and because he was friends with Orci and Kurtzman. The writing trio were paid $8 million. Screenwriting was interrupted by the 2007&ndash;2008 Writers Guild of America strike, but to avoid production delays the writers spent two weeks writing a treatment, which they handed in the night before the strike began, and Bay expanded the outline into a sixty-page scriptment, fleshing out the action, adding more jokes, as well as selecting the majority of new characters. The three writers spent four months finishing the screenplay while &quot;locked&quot; in two hotel rooms by Bay: Kruger wrote in his own room and the trio would check on each others' work twice a day.
		</p>
        <br />
        <p>
Orci described the film's theme as &quot;being away from home&quot;, with the Autobots contemplating living on Earth as they cannot restore Cybertron, while Sam goes to college. He wanted the focus between the robots and humans &quot;much more evenly balanced&quot;, &quot;the stakes [to] be higher&quot;, and more focused on the science fiction elements. Lorenzo di Bonaventura said that in total, there are around forty robots in the film, while ILM's Scott Farrar has said there are actually sixty. Orci added he wanted to &quot;modulate&quot; the humor more, and felt he managed the more &quot;outrageous&quot; jokes by balancing it with a more serious plot approach to the Transformers' mythology. Bay concurred that he wanted to please fans by making the tone darker, and that &quot;moms will think its safe enough to bring the kids back out to the movies&quot; despite his trademark sense of humor. Kurtzman created the film's title. The filmmakers considered incorporating the comics' character of G. B. Blackrock, but Bay considered the name too cartoonish.
		</p>
