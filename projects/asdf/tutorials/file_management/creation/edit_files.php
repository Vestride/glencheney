<h1>Editing Files</h1>
<p>
Earlier, we learned the cat command, which allows you 
to simply make and add onto text files.  However, 
it cannot edit them easily.  That's where pico comes in.  
pico works a lot like Word, without any formatting options.
</p>
<p>
To make a new file or open an existing one, enter 
"pico (file name)".  This will open up the pico editor.  
You can now type a message, and move around the text with the arrow keys.
</p>
