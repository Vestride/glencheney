<h1>Reminder Commands</h1>
<p>
If you ever forget the details of a command, there are two UNIX commands to help you.  
<span style="font-family:courier;">whatis (command name)</span> will give you a brief reminder of what that command does.  
<span style="font-family:courier;">man (command name)</span> will show you the entire documentation for the command, which 
will be extremely long and detailed.  If you want to know everything about a command, 
<span style="font-family:courier;">man</span> is the way to go. 
</p>

