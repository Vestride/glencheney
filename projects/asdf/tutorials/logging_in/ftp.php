</p>
<p> 
In the Quickconnect bar, enter the server you wish to connect to, 
the username and password you have or were given, and the port number. 
If you are using regular FTP, use port 21. If you are using SFTP use port 22. 
If you are unsure, try port 22 first, and if that doesn't work use port 21. 
</p>
<p>
If you successfully connected, you should see a list of the files on the 
server in the righthand panel. You can move around the file structure 
just like you do in your local files that are physically on your computer. 
You can download any file on the server and upload any file to the 
server from your computer. To learn more about this go to our 
<a href="http://nova.it.rit.edu/~409Dan-asdf/tutorials/internet_concerns/?tutorial=uploading">
Uploading Files</a> section of the tutorial.
</p>
