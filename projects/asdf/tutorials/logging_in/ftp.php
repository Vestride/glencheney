<h1>FTP</h1>
<p>
There are several good, free FTP programs for download. 
For Windows, you can use <a href="http://www.smartftp.com/">
SmartFTP</a>, <a href="http://filezilla-project.org/">FileZilla</a>, 
or <a href="http://winscp.net/eng/index.php">WinSCP</a>. 
For Mac, you can use <a href="http://filezilla-project.org/">
FileZilla</a>, <a href="http://fetchsoftworks.com/">Fetch</a> 
(not free), or <a href="http://cyberduck.ch/">Cyberduck</a>. 
Linux runs many, the most prevalent of which is 
<a href="http://filezilla-project.org/">FileZilla</a>. You can 
also get a cross-platform addon for Firefox, called 
<a href="http://fireftp.mozdev.org/">FireFTP</a>.
Since FileZilla runs on all three, that is the one we will introduce you to.
</p>
<p>
When you run FileZilla, you will see a Quickconnect bar at the top, 
a list of the files on your computer on the left, and a blank space 
off to the right where the files on the server will be shown.
