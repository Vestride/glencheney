<h1>Ports</h1>

<p>
To connect to a remote computer you must go though what is called a port. 
Ports are nothing more than a way to say to the computer that you are connecting 
to, "Hey there, I'm connecting to you for this!" Every port has a different meaning, 
and there are hundreds. The only ones you will be using in the context of this 
tutorial are 21 and 22. 21 is for FTP. 22 is for SSH and Secure FTP, or SFTP. 
It is a more common version of FTP because, you guessed it, it's more secure.
</p>
