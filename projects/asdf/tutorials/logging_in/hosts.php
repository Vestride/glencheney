<h1>Hosts</h1>
<p>
In order to connect to a UNIX server you must have a 
server to connect to. Presumably, you want to connect 
so you can edit your files on a server. In order to 
do that you need to acquire that space. This is done 
with a host. Hosts are companies that rent space on 
their servers to people who can't afford to or don't 
know how to set up their own server. If you know how to 
set up your own server, you probably wouldn't be here. 
So, we will assume you are reading this tutorial in the 
context of wanting to connect to a host's server. 
<br />(If you do want to try setting up your own server, check out 
<a href="http://lifehacker.com/124212/geek-to-live--how-to-set-up-a-personal-home-web-server">
this</a> outside tutorial)
</p>
<p>
You can rent space from companies such as 
<a href="http://www.ipage.com/ipage/lp/special.bml?LinkName=No_Name">
iPage</a>, <a href="http://www.godaddy.com/Hosting/web-hosting.aspx?isc=cjchstsale">
GoDaddy</a>, 
<a href="http://smallbusiness.yahoo.com/webhosting/hosting3.php?p=WHPROMO&amp;s_affiliate=wh_acq_links_20090901?AID=10432499&amp;PID=1759994">
Yahoo! Hosting</a>, or many others for a monthly or 
yearly fee. You can also find some free web hosts, 
such as <a href="http://www.000webhost.com/">000webhost</a>. 
Different companies will have different options 
(like emails linked to your site, PHP, MySQL, or 
better up-time percentages and storage space). Choose 
which one you want based on what options are 
most important to you.
</p>