<h1>Your Account</h1>
<p>
If this is your first time logging in, you might have been given a 
default password. It would be a good idea to change it to something 
that you can easily remember. UNIX also may periodically require 
you to change it for security reasons.
</p>
<p>
To change your password, type in <span style="font-family:courier">
passwd</span>. You will be required to type in your old password 
for security reasons (which, again, you will not see). 
UNIX will then prompt you to enter your new password twice to 
make sure you don't mess up.
