Some UNIX systems have a minimum 
password length or require numbers or special symbols. The system 
won't accept a password that is too short or that doesn't have 
the required variety of characters.
</p>
<p>
On most UNIX systems every user has their own directory (folder), 
usually with the name of their username. It also is usually the 
default directory you are in when you log in (more on navigating 
directories later in the tutorial). Inside this folder is where 
you will do almost all of your work in UNIX.
</p>