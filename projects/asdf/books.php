<h2>Unix Power Tools, Third Edition</h2>
<p>
By Jerry Peek, Shelley Powers, Tim O'Reilly, Mike Loukides<br />
Publisher:O'Reilly Media<br />
Released: October 2002<br />
Pages: 1160<br />
</p>
<p>
The latest edition of this best-selling favorite is loaded 
with advice about almost every aspect of Unix, covering all 
the new technologies that users need to know. In addition to 
vital information on Linux, Darwin, and BSD, Unix Power Tools 
3rd Edition now offers more coverage of bash, zsh, and other 
new shells, along with discussions about modern utilities and 
applications. Several sections focus on security and Internet 
access. And there is a new chapter on access to Unix from Windows, 
addressing the heterogeneous nature of systems today. You'll 
also find expanded coverage of software installation and packaging, 
as well as basic information on Perl and Python.
</p>
</div>

<div>
<img src="nongeeks.jpg" style="float:left;margin:10px;" alt="Ubuntu Linux for Non-Geeks" />
<h2>Ubuntu Linux for Non-Geeks</h2>
<p>
By Rickford Grant
Publisher: No Starch Press
Released: August 2006
Pages: 360
</p>
<p>
This newbie's guide to Ubuntu lets readers learn by doing. 
Using immersion-learning techniques favored by language 
courses, step-by-step projects build upon earlier tutorial 
concepts, stimulating the brain and increasing the reader's 
understanding. It also covers all the topics likely to be of 
interest to an average desktop user, such as installing new 
software via Synpatic; Internet connectivity; working with 
removable storage devices, printers, and scanners; and 
handling DVDs, audio files, and even iPods. It also eases 
readers into the world of commands, thus allowing them to 
work with Java, Python or other script-based applications; 
converting RPMs to DEB files; and compiling software from source.
</p>
</div>

<div>
<img src="nutshell.jpg" style="float:left;margin:10px;" alt="UNIX in a Nutshell" />
<h2>Unix in a Nutshell</h2>
<p>
by Arnold Robbins
Publisher: O'Reilly Media, Inc.
Released: July 2008
Pages: 906
</p>
<p>
Detailing all Unix commands and options, the informative 
guide provides generous descriptions and examples that 
put those commands in context. Here are some of the new 
features you'll find in "Unix in a Nutshell, Fourth Edition": 
Solaris 10, the latest version of the SVR4-based 
operating system, GNU/Linux, and Mac OS X; Bash 
shell (along with the 1988 and 1993 versions of ksh); 
tsch shell (instead of the original Berkeley csh); 
package management programs, used for program 
installation on popular GNU/Linux systems, Solaris and 
Mac OS X; GNU Emacs Version 21; Introduction to source 
code management systems; concurrent versions system; 
subversion version control system; GDB debugger. As 
Unix has progressed, certain commands that were once 
critical have fallen into disuse. To that end, the 
book has also dropped material that is no longer relevant, 
keeping it taut and current. If you're a Unix user or 
programmer, you'll recognize the value of this complete, 
up-to-date Unix reference. It includes chapter overviews, 
specific examples, and detailed commands.
</p>
</div>