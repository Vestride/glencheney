<h2>UNIX in a Nutshell</h2>
<p>
by Arnold Robbins<br />
Publisher: O'Reilly Media, Inc.<br />
Released: July 2008<br />
Pages: 906<br />
</p>
<p>
Detailing all UNIX commands and options, the informative 
guide provides generous descriptions and examples that 
put those commands in context. Here are some of the new 
features you'll find in "UNIX in a Nutshell, Fourth Edition": 
Solaris 10, the latest version of the SVR4-based 
operating system, GNU/Linux, and Mac OS X; Bash 
shell (along with the 1988 and 1993 versions of ksh); 
tsch shell (instead of the original Berkeley csh); 
package management programs, used for program 
installation on popular GNU/Linux systems, Solaris and 
Mac OS X; GNU Emacs Version 21; Introduction to source 
code management systems; concurrent versions system; 
subversion version control system; GDB debugger. As 
UNIX has progressed, certain commands that were once 
critical have fallen into disuse. To that end, the 
book has also dropped material that is no longer relevant, 
keeping it taut and current. If you're a UNIX user or 
programmer, you'll recognize the value of this complete, 
up-to-date UNIX reference. It includes chapter overviews, 
specific examples, and detailed commands.
</p>
</div>