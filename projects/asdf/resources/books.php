<h2>UNIX Power Tools, Third Edition</h2>
<p>
By Jerry Peek, Shelley Powers, Tim O'Reilly, Mike Loukides<br />
Publisher:O'Reilly Media<br />
Released: October 2002<br />
Pages: 1160<br />
</p>
<p>
The latest edition of this best-selling favorite is loaded 
with advice about almost every aspect of UNIX, covering all 
the new technologies that users need to know. In addition to 
vital information on Linux, Darwin, and BSD, UNIX Power Tools 
3rd Edition now offers more coverage of bash, zsh, and other 
new shells, along with discussions about modern utilities and 
applications. Several sections focus on security and Internet 
access. And there is a new chapter on access to UNIX from Windows, 
addressing the heterogeneous nature of systems today. You'll 
also find expanded coverage of software installation and packaging, 
as well as basic information on Perl and Python.
</p>
</div>

